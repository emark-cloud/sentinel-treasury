// Fix: @make-software/cspr-trade-mcp(-sdk) are ESM and do
//   `import { Args, CLValue, ... } from 'casper-js-sdk'`
// but casper-js-sdk 5.0.12 ships only UMD bundles (lib.node.js) with no `import`
// condition, so Node's ESM loader cannot detect named exports and startup throws
//   SyntaxError: Named export 'Args' not found.
//
// We add an ESM wrapper that re-exports the named bindings from the UMD bundle via
// createRequire, and point a new `import` condition at it. Idempotent; safe to re-run.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const sdkDir = join(here, 'node_modules', 'casper-js-sdk');
const pkgPath = join(sdkDir, 'package.json');
if (!existsSync(pkgPath)) {
  console.error('[patch] casper-js-sdk not installed; skipping');
  process.exit(0);
}

// Named bindings imported by the MCP bundles (incl. aliased uses of the same export).
const NAMES = [
  'Args', 'CLValue', 'CLTypeUInt8', 'CLTypeKey', 'Key',
  'PublicKey', 'HttpHandler', 'RpcClient', 'PurseIdentifier',
  'Transaction', 'SessionBuilder', 'ContractCallBuilder',
  'PrivateKey', 'KeyAlgorithm',
];

const wrapper = `// AUTO-GENERATED by patch-casper-esm.mjs — ESM named re-exports over the UMD bundle.
import { createRequire } from 'node:module';
const require = createRequire(import.meta.url);
const sdk = require('./lib.node.js');
export const { ${NAMES.join(', ')} } = sdk;
export default sdk;
`;
const wrapperFile = join(sdkDir, 'dist', 'esm-named.mjs');
writeFileSync(wrapperFile, wrapper);

const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
const dot = pkg.exports && pkg.exports['.'] ? pkg.exports['.'] : {};
// `import` must precede `default`; rebuild the conditions object in correct order.
pkg.exports = {
  '.': {
    types: dot.types ?? './dist/index.d.ts',
    'react-native': dot['react-native'],
    browser: dot.browser,
    import: './dist/esm-named.mjs',
    require: dot.require ?? './dist/lib.node.js',
    default: dot.default ?? './dist/lib.node.js',
  },
};
// drop undefined keys
pkg.exports['.'] = Object.fromEntries(
  Object.entries(pkg.exports['.']).filter(([, v]) => v != null),
);
writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n');
console.error('[patch] casper-js-sdk ESM import condition + wrapper installed');
